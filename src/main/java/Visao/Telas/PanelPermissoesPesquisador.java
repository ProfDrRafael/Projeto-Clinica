    public HashMap<String, List<String>> obterOpcoesSelecionadas() {
        HashMap<String, List<String>> selecionados = new HashMap<>();

        // Helper para adicionar item à lista
        BiConsumer<String, String> adicionar = (chave, valor) -> {
            selecionados.computeIfAbsent(chave, k -> new ArrayList<>()).add(valor);
        };

        // Gráficos
        if (cbGenero.isSelected()) adicionar.accept("Gráfico", "Gênero");
        if (cbEstadoCivil.isSelected()) adicionar.accept("Gráfico", "Estado Civil");
        if (cbRaca.isSelected()) adicionar.accept("Gráfico", "Raça/Cor/Etnia");
        if (cbEvolucaoAtendimento.isSelected()) adicionar.accept("Gráfico", "Evolução Atendimentos");
        if (cbAgendadosRealizados.isSelected()) adicionar.accept("Gráfico", "Agendados vs Realizados");
        if (cbPacienteComparecimento.isSelected()) adicionar.accept("Gráfico", "Paciente Comparecimento");
        if (cbTempoMedio.isSelected()) adicionar.accept("Gráfico", "Tempo Médio Atendimento");
        if (cbPacienteIdade.isSelected()) adicionar.accept("Gráfico", "Paciente por Idade");
        if (cbListaEspera.isSelected()) adicionar.accept("Gráfico", "Lista de Espera");
        if (cbOcupacaoSala.isSelected()) adicionar.accept("Gráfico", "Ocupação das Salas");

        // Tabelas
        if (cbPacientesTabela.isSelected()) adicionar.accept("Tabelas", "Todos os Pacientes");
        if (cbListaEsperaTabela.isSelected()) adicionar.accept("Tabelas", "Lista de Espera Geral");
        if (cbListaEsperaEspecificaTabela.isSelected()) adicionar.accept("Tabelas", "Lista de Espera Específica");
        if (cbAgendamentosTabela.isSelected()) adicionar.accept("Tabelas", "Agendamentos");
        if (cbAtendimentosTabela.isSelected()) adicionar.accept("Tabelas", "Atendimentos");
        if (cbEstagiariosTabela.isSelected()) adicionar.accept("Tabelas", "Todos os Estagiários");
        if (cbUsuariosTabela.isSelected()) adicionar.accept("Tabelas", "Todos os Usuários");

        // Páginas
        if (cbPaginaAtendimento.isSelected()) adicionar.accept("Formulário", "Atendimento");
        if (cbPaginaCalendario.isSelected()) adicionar.accept("Formulário", "Calendário");
        if (cbPaginaVincular.isSelected()) adicionar.accept("Formulário", "Vincular Estagiário");
        if (cbPaginaUsuario.isSelected()) adicionar.accept("Formulário", "Usuário");
        if (cbPaginaProntuario.isSelected()) adicionar.accept("Formulário", "Prontuário");
        if (cbPaginaAgenda.isSelected()) adicionar.accept("Formulário", "Agenda");

        return selecionados;
    }
    
