    private void btSalvarActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_btSalvarActionPerformed
        String textoRelatorio = tpTextoRelatorio.getText();
        if (textoRelatorio == null || textoRelatorio.trim().isEmpty()) {
            MessagesAlert messagesAlert = new MessagesAlert();
            messagesAlert.showErrorMessage("O texto do relato não pode estar vazio.");
            return;
        }

        if (onRelatoSubmetido != null) {
            onRelatoSubmetido.accept(textoRelatorio);
        }
    }// GEN-LAST:event_btSalvarActionPerformed
    private void tfIdadeKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tfIdadeKeyTyped
        char c = evt.getKeyChar();

        if (!Character.isDigit(c)) {
            evt.consume();
        }
    }//GEN-LAST:event_tfIdadeKeyTyped


