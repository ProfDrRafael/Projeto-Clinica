    public void LoadProgressBar(){
        for(int i=0; i<=100; i++){
            try {
                Thread.sleep(80);
                
                pbCarregando.setValue(i);
                
                if (i%2 == 0) {
                    lbCarregando.setText("Carregando..");
                } else {
                    lbCarregando.setText("Carregando...");
                }
            } catch (InterruptedException e) {
                Logger.getLogger(PageProgressBar.class.getName()).log(Level.SEVERE, null, e);
            }
        }
    }
    
